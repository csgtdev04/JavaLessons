public class Loops {
    public static void main(String[] args) {
        // loop that executes some task over and over until your condition
        // condition : print hi, 10 times (0 -> 9; 1 -> 10)
        // 2 types: for (definite loop), while (indefinite)

        for (int i = 1; i <= 50; i++) {
            System.out.println("Hi");
        }
    }
}
